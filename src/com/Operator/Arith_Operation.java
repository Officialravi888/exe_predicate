package com.Operator;

public class Arith_Operation {
    public static void main(String[] args) {


        int a = 12, b = 17, result;
        System.out.println("a = " + a + ", b = " + b);
        result = a + b;
        System.out.println("Addition : " + a + " + " + b + " = " + result);
        result = a - b;
        System.out.println("Addition : " + a + " - " + b + " = " + result);


    }
}
